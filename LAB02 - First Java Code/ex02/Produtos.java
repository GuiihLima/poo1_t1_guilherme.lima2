package ex02;

public class Produtos {
    private int codigo;
    private String nome;
    private String fabricante;
    private float preço;
    
    public Produtos(int codigo, String nome, String fabricante, float preço) {
        this.codigo = codigo;
        this.nome = nome;
        this.fabricante = fabricante;
        this.preço = preço;
    }
}